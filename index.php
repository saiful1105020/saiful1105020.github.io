            <p>In this term project, we did an experiment on Neural Machine Translation(NMT) for Bangla to English Translation. We used a moderate size dataset containing 4379 sentence translations from English to Bangla. We used seq2seq encoder-decoder model containing Word2Vec and LSTMs with and without attention for small epochs. With finely tuned hyperparameters, we observed that using Bahdanau's attention with vanilla encoder-decoder model improves BLEU score for Bangla to English translation.</a></p>
