            <h3 class="mb-0">SAINT : Self-Attention Augmented Inception-Inside-Inception Network Improves Protein Secondary Structure Prediction</h3>
            <div class="subheading mb-3">Co-authors: Sazan Mahbub, Dr. Md Saifur Rahman, Dr. Md Shamsuzzoha Bayzid</div>
            <p>In this work, we present SAINT, a highly accurate method for protein Q8 structure prediction, which incorporates self-attention mechanism (a concept from natural language processing) with the Deep Inception-Inside-Inception (Deep3I) network in order to effectively capture both the short-range and long-range dependencies among the amino acid residues. SAINT offers a more interpretable framework than the typical black-box deep neural network methods. We report, on an extensive evaluation study, the performance of SAINT in comparison with the existing best methods on a collection of benchmark dataset (CB513, CASP10, and CASP11). We have made SAINT freely available as open source code at <a href="https://github.com/SAINTProtein/SAINT.">this link.</a></p>
